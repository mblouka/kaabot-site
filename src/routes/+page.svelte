<h3>Overview</h3>

<p>
  Kaab'ot is a little Discord bot that provides your guild with knowledge on Islam and the Holy
  Quran, as well as many tools for understanding and sourcing Islamic knowledge. It is free and
  open-source, licensed under the <a href="https://github.com/mblouka/kaabot/blob/main/LICENSE"
    >GNU Affero General Public License</a
  >.
</p>

<p>
  The source code to Kaab'ot, as well as its documentation, can be retrieved from its <a
    href="https://github.com/mblouka/kaabot">repository</a
  >. You can add the official instance to your Discord guild by pressing the "Add" button above or
  clicking
  <a href="https://add.kaabot.org"> here</a>, but you may also self-host your own instance of the
  bot instead.
</p>

