<h3>Cheat sheet</h3>
<p>
  This list is irregularly updated and does not list all commands, only core ones. Please consult
  the repository for up-to-date information.
</p>
<ul>
  <li>
    <code>/verse</code> retrieves a verse from the Holy Quran.
    <ul>
      <li>An English, Arabic, and Urdu translation is automatically provided.</li>
      <li>
        The <code>analyze</code> parameter can be passed to also include an analysis of each Arabic word.
      </li>
    </ul>
  </li>
  <li>
    <code>/sermons</code> retrieves the latest Friday sermon.
    <ul>
      <li>
        <code>/sermons get</code> provides both a permalink from MTA and a direct download link to a
        1920x1080 (full HD) mp4 video of the sermon.
      </li>
      <li>
        <code>/sermons list</code> lists all ten latest Friday sermons.
      </li>
    </ul>
  </li>
  <li>
    <code>/library</code> retrieves Islamic books and writings from
    <a href="https://alislam.org">alislam.org</a>.
    <ul>
      <li>
        <code>/library search</code> searches across the library based on the provided query (e.g., "jesus").
      </li>
      <li>
        <code>/library author</code> lists information about the provided uauthor.
      </li>
      <li>
        <code>/library list-authors</code> lists all indexed authors.
      </li>
      <li>
        <code>/library list-books</code> lists all books from the provided author.
      </li>
      <li>
        <code>/library get</code> retrieves information about the provided book identifier and a download
        link to it.
      </li>
    </ul>
  </li>
  <li>
    <code>/calendar</code> translates dates between the Gregorian and Hijri calendars.
  </li>
</ul>

